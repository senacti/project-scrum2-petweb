     <h1>PELUQUERÍA</h1>
     <h3>
        La peluquería en perros es un servicio especializado que se enfoca en el cuidado estético y la higiene de las mascotas caninas. Incluye una variedad de tratamientos como corte de pelo, baño, cepillado, limpieza de orejas, corte de uñas y, en algunos casos, tratamientos específicos para el cuidado de la piel y el pelaje. El objetivo principal es mantener a los perros limpios, cómodos y saludables, al tiempo que realzan su apariencia estética según las preferencias del dueño y las necesidades de la raza.</h3>
