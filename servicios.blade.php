 <h1>BAÑO</h1>
 <h3>El baño en perros es un procedimiento fundamental para mantener la higiene y la salud de tu mascota. Consiste en limpiar el pelaje y la piel del perro con agua y champú específico para perros, eliminando la suciedad, el exceso de grasa y los posibles parásitos que puedan acumularse. Es importante realizar el baño de manera regular, pero no excesiva, para evitar problemas dermatológicos y mantener el equilibrio natural de la piel y el pelaje de tu perro. </h3>
